'''

Oops concepts:
1. Class
2. Object
3. Inheritance
4. Polymorphism
5. Encapsulation
6. Abstraction


Class:
Class is a blueprint or a template for creating objects.it has state and behavior.
state: attributes or properties
behavior: methods or functions


Object:
Object is an instance of a class.it is actual entity created based on the class blueprint.

the memory is created when the object is created.without the object we cannot access the class members.


attributes: attributes are variable that hold data for the object created from the class. these can be instance attributes or class attributes.(shared by all the intances of the class).

methods: methods are functions that are defined the behaviour of the class. they typically operate on the data(attributes) of the object or class and  can be used to modify or interact with the object's state.


self: self is a reference to the current instance of the class. it is used to access the attributes and methods of the class. it is the first parameter of any instance method.

'''