# Write a Python program to create a tuple with multiple data types.

# Creating a tuple with multiple data types
my_tuple = (10, "Hello", 3.14, True, [1, 2, 3], {"name": "Mithlesh"})

# Printing the tuple
print("Tuple with multiple data types:")
print(my_tuple)

# Accessing individual elements
print("\nAccessing individual elements:")
print("Integer:", my_tuple[0])
print("String:", my_tuple[1])
print("Float:", my_tuple[2])
print("Boolean:", my_tuple[3])
print("List:", my_tuple[4])
print("Dictionary:", my_tuple[5])