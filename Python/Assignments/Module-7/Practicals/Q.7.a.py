# Write a Python program to update a value in a dictionary.

# Creating a dictionary
my_dict = {
    "name": "Mithlesh",
    "age": 25,
    "city": "Surat",
    "occupation": "Student",
    "salary": 50000,
    "is_student": True
}

# Printing the dictionary
print("Dictionary with 6 key-value pairs:")
print(my_dict)

# Updating a value in the dictionary
my_dict["age"] = 26
print("\nAfter updating age:", my_dict)
