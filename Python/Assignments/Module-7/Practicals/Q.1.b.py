# Write a Python program to access elements at different index positions

# Creating a list
my_list = [10, "Hello", 3.14, "Python", 50]

# Accessing elements using index positions
print("First element:", my_list[0])      # index 0
print("Second element:", my_list[1])     # index 1
print("Third element:", my_list[2])      # index 2
print("Last element:", my_list[-1])      # negative index
print("Second last element:", my_list[-2])

# Accessing a range of elements (slicing)
print("\nSliced elements (index 1 to 3):", my_list[1:4])