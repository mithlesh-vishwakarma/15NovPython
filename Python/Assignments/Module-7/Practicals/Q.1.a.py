# Write a Python program to create a list with elements of multiple data types (integers,
# strings, floats, etc.).

# Creating a list with multiple data types
my_list = [10, "Hello", 3.14, True, [1, 2, 3], {"name": "Mithlesh"}]

# Printing the list
print("List with multiple data types:")
print(my_list)

# Accessing elements
print("\nAccessing individual elements:")
print("Integer:", my_list[0])
print("String:", my_list[1])
print("Float:", my_list[2])
print("Boolean:", my_list[3])
print("List:", my_list[4])
print("Dictionary:", my_list[5])