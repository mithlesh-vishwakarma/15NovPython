# Write a Python program to access values using keys from a dictionary.

# Creating a dictionary
my_dict = {
    "name": "Mithlesh",
    "age": 25,
    "city": "Surat",
    "occupation": "Student",
    "salary": 50000,
    "is_student": True
}

# Printing the dictionary
print("Dictionary with 6 key-value pairs:")
print(my_dict)

# Accessing values using keys
print("\nAccessing values using keys:")
print("Name:", my_dict["name"])
print("Age:", my_dict["age"])
print("City:", my_dict["city"])
print("Occupation:", my_dict["occupation"])
print("Salary:", my_dict["salary"])
print("Is Student:", my_dict["is_student"])